// This program uses a char variable and several character literals.
#include <iostream>
using namespace std;

int main()
{
   char letter; // Used for storing individual characters, such as letters, digits, or symbols.

   letter = 'A'; // The 'A' is a character literal
   cout << letter << endl;
	
   letter = 'B'; // The 'B' is a character literal
   cout << letter << endl;
   return 0;
}
