// This program uses integer literals, string literals, and a variable.
#include <iostream>
using namespace std;

int main()
{
	int apples; //here is the variable

	apples = 20; //assigning value to the variable
	cout << "On Sunday we sold " << apples << " bushels of apples. \n";
    // the sentences enclosed withing the double quotations are the string literals
    // the 20 and 15 are examples of inter literals
	
	apples = 15; //assigning value to the variable
	cout << "On Monday we sold " << apples << " bushels of apples. \n";
	return 0;
}
